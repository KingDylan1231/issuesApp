<?php
session_start();
require '../database/database.php'; 

$error = '';

$pdo = Database::connect(); 

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $email = trim($_POST['email']);
    $password = trim($_POST['password']);

    if (!empty($email) && !empty($password)) {
        try {
            // Query user by email
            $stmt = $pdo->prepare("SELECT id, fname, lname, pwd_hash FROM iss_persons WHERE email = :email");
            $stmt->bindParam(':email', $email, PDO::PARAM_STR);
            $stmt->execute();
            $user = $stmt->fetch(PDO::FETCH_ASSOC);

            if ($user) {
                $stored_hash = $user['pwd_hash'];

                // Hash the password directly without salt (since salt is not part of your system)
                $hashed_input_password = md5($password);

                // Compare the computed hash with the stored hash
                if ($hashed_input_password === $stored_hash) {
                    $_SESSION['user_id'] = $user['id'];
                    $_SESSION['user_name'] = $user['fname'] . ' ' . $user['lname'];
                    
                    Database::disconnect(); 
                    header("Location: issues_list.php");
                    exit();
                } else {
                    $error = "Invalid email or password.";
                }
            } else {
                $error = "Invalid email or password.";
            }
        } catch (PDOException $e) {
            $error = "Database error: " . $e->getMessage();
        }
    } else {
        $error = "Please enter both email and password.";
    }
}

Database::disconnect();
